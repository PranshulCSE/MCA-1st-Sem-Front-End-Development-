// Now lets See Some Basic Operations of Array
// 1. Finding Length 
let arr1=[1,2,3,4,5,6];
console.log(arr1.length); 
// Output: 6
// 2. Inserting Element at last 
let arr2=[1,2,3,4,5,6];
arr2.push(7);
console.log(arr2); 
// 3. Deleting Element from Last
let arr3=[1,2,3,4,5,6];
arr3.pop();
console.log(arr3); 
//4. Inserting at Front
let arr4=[1,2,3,4,5,6];
arr4.unshift(0);
console.log(arr4); 
//5. Deletion from Front
let arr5=[0,1,2,3,4,5,6];
arr5.shift();
console.log(arr5); 
