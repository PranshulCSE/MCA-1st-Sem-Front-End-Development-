// In Javascript Math is a object and there are various Methods associated with it, lets discuss them
console.log(Math.PI); // Give the Value of PI i.e 3.14 somelike
console.log(Math.abs(-132001));  // Give the absoulute value
console.log(Math.LOG10E);  // Gives Logarithm of 10 to the Base E
console.log(Math.SQRT2); // Gives Sqaureroot of 2
console.log(Math.sqrt(15)); // Gives Sqaure root of any number
console.log(Math.ceil(8.524)); // Gives the nearest upper number like if we having 8.2 then it will give 9
console.log(Math.floor(8.424)); // Gives the nearest lower number like if we having 8.7 then it will give 8
console.log(Math.random()); // Generates the random number between 0 and 1;
console.log(Math.max(10,14,15,3,8,4)); // Gives Maximum Number
console.log(Math.min(9,14,15,3,8,4));  // Gives Minimum Number
