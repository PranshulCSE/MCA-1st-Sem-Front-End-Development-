//  Getting Information from a Date Object (Getters)
const now=new Date();
console.log(now.getFullYear()); // Output:2025
console.log(now.getMonth()); // Output:10
console.log(now.getDate());// Output:10
console.log(now.getTime());// Output:1762768837888
console.log(now.getHours());// Output:15
console.log(now.getMinutes());// Output:30
console.log(now.getSeconds());// Output:37
console.log(now.getMilliseconds());// Output:888
console.log(now.getDay());// Output:1

